        <p><strong>1. 医療法人の売上規模が一定以上：</strong>一般論として、医療法人の年間売上が1億円を超えるあたりから、MS法人を活用するメリットが出てくるとされています。売上が小さい段階では、法人を2つ維持するコスト（税理士顧問料・法人住民税の均等割等）がメリットを上回る可能性があります。</p>

